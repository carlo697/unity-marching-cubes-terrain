  private void RegenerateIfNeeded() {
    if (m_rebuildFlag || m_points == null || !m_mesh) {
      InstantRegenerate();
      m_rebuildFlag = false;
    }
  }

  private void OnValidate() {
    FlagToRegenerate();
  }

  private void OnDrawGizmos() {
    RegenerateIfNeeded();

    if (!drawGizmos) return;

    for (int z = 0; z < resolution.z; z++) {
      for (int y = 0; y < resolution.y; y++) {
        for (int x = 0; x < resolution.x; x++) {
          Vector3 normalizedStepPosition = GetNormalizedPositionOfPoint(x, y, z);
          Vector3 globalPointPosition = transform.TransformPoint(normalizedStepPosition);

          float value = m_points[x, y, z];
          Gizmos.color = new Color(value, value, value);
          Gizmos.DrawCube(globalPointPosition, Vector3.one * gizmosSize);
        }
      }
    }
  }
